# Module 3: Counterfactual Sensitivity
# ════════════════════════════════════════════════════════════════════

@dataclass
class CounterfactualResult:
    """Diff between baseline and modified scenario."""
    scenario_name: str
    baseline_overload_count: int
    modified_overload_count: int
    baseline_infeasible_count: int
    modified_infeasible_count: int
    holding_cost_delta: float
    summary: str

    def to_dict(self) -> dict:
        return {
            "scenario_name": self.scenario_name,
            "baseline_overload_count": self.baseline_overload_count,
            "modified_overload_count": self.modified_overload_count,
            "baseline_infeasible_count": self.baseline_infeasible_count,
            "modified_infeasible_count": self.modified_infeasible_count,
            "holding_cost_delta": self.holding_cost_delta,
            "summary": self.summary,
        }


async def counterfactual_capacity_increase(
    db: AsyncSession,
    mps_id: str,
    work_center_id: str,
    multiplier: float = 1.2,  # +20%
) -> CounterfactualResult:
    """What if we increased work-center X's capacity by `multiplier`?

    Per Saltelli et al. (2008): this is local sensitivity analysis;
    a one-factor-at-a-time (OAT) perturbation.

    Limitations:
      • OAT misses interactions (Saltelli §2.5); for global SA see
        Sobol indices.
      • Capacity increase modeled as a single-period change; reality
        is more nuanced (training cost, ramp-up time).

    Returns CounterfactualResult comparing baseline vs modified.
    """
    from app.services.capacity_aware_mrp import (
        run_capacity_aware_mrp, dixon_silver_capacity_feasible,
        build_resource_profile, compute_work_center_load,
    )

    # Baseline run
    baseline = await run_capacity_aware_mrp(db, mps_id)

    # Modified: increase target WC's effective capacity
    wcs = (await db.execute(
        select(WorkCenter).where(WorkCenter.is_active == True)
    )).scalars().all()

    # We can't actually mutate the DB; simulate by giving the target WC
    # an inflated efficiency factor in a copy
    class _MockWC:
        def __init__(self, src, eff):
            self.id = src.id
            self.code = src.code
            self.efficiency = eff
            self.capacity_per_day = src.capacity_per_day
            self.alternate_group = src.alternate_group

    modified_wcs = {}
    for wc in wcs:
        if wc.id == work_center_id:
            modified_wcs[wc.id] = _MockWC(wc, (wc.efficiency or 1.0) * multiplier)
        else:
            modified_wcs[wc.id] = wc

    # Re-run Dixon-Silver with mutated capacity
    # We need planned orders — extract from baseline's MRP items
    from app.models.mps_mrp import MrpItem
    mrp_items = (await db.execute(
        select(MrpItem)
        .where(MrpItem.mrp_master_id ==
               # latest run from baseline pipeline
               (await db.execute(
                   select(MrpMaster.id).where(MrpMaster.mps_master_id == mps_id)
                   .order_by(MrpMaster.created_at.desc())
               )).scalar_one_or_none()
              )
    )).scalars().all()

    # Aggregate planned receipts by part → planned_orders[part_id][t]
    unique_periods = sorted({i.period for i in mrp_items})
    period_to_idx = {p: i for i, p in enumerate(unique_periods)}
    planned: Dict[str, List[float]] = {}
    for it in mrp_items:
        if it.part_id not in planned:
            planned[it.part_id] = [0.0] * len(unique_periods)
        planned[it.part_id][period_to_idx[it.period]] += it.planned_order_receipt

    # Translate to product-keyed for capacity calc
    products = (await db.execute(select(Product))).scalars().all()
    parts = (await db.execute(select(Part))).scalars().all()
    pno_to_pid = {p.product_no: p.id for p in products}
    part_to_prod: Dict[str, str] = {}
    for pt in parts:
        if pt.part_no in pno_to_pid:
            part_to_prod[pt.id] = pno_to_pid[pt.part_no]

    by_prod: Dict[str, List[float]] = {}
    for part_id, qts in planned.items():
        prod_id = part_to_prod.get(part_id)
        if prod_id is None:
            continue
        if prod_id not in by_prod:
            by_prod[prod_id] = [0.0] * len(qts)
        for t, q in enumerate(qts):
            by_prod[prod_id][t] += q

    profile = await build_resource_profile(db)

    # Run Dixon-Silver with modified capacity
    _adjusted, modified = dixon_silver_capacity_feasible(
        by_prod, profile, modified_wcs,
        horizon_periods=max(12, len(unique_periods)),
    )

    # Compute deltas
    delta_holding = modified.holding_cost_penalty - baseline.holding_cost_penalty

    summary = (
        f"WC {work_center_id} 產能 ×{multiplier}：超載期數 "
        f"{len(baseline.overloads)} → {len(modified.overloads)}；"
        f"不可行期數 {len(baseline.infeasible_periods)} → "
        f"{len(modified.infeasible_periods)}；"
        f"持有成本懲罰變化 {delta_holding:+.2f}"
    )

    return CounterfactualResult(
        scenario_name=f"capacity_{work_center_id}_x{multiplier:g}",
        baseline_overload_count=len(baseline.overloads),
        modified_overload_count=len(modified.overloads),
        baseline_infeasible_count=len(baseline.infeasible_periods),
        modified_infeasible_count=len(modified.infeasible_periods),
        holding_cost_delta=delta_holding,
        summary=summary,
    )

@dataclass
class ExplanationNode:
    """A single node in the demand provenance tree.

    node_type ∈ {sales_order, mps_entry, mrp_item, bom_explosion}
    """
    node_type: str
    entity_id: str
    label: str  # human-readable
    qty: float
    period: Optional[str] = None
    detail: Dict = field(default_factory=dict)
    children: List["ExplanationNode"] = field(default_factory=list)

    def to_dict(self) -> dict:
        return {
            "node_type": self.node_type,
            "entity_id": self.entity_id,
            "label": self.label,
            "qty": self.qty,
            "period": self.period,
            "detail": self.detail,
            "children": [c.to_dict() for c in self.children],
        }

    def render_tree(self, depth: int = 0) -> str:
        """ASCII tree visualization. Useful for debugging and CLI output."""
        prefix = "  " * depth + ("└─ " if depth > 0 else "")
        line = f"{prefix}[{self.node_type}] {self.label} (qty={self.qty:g}"
        if self.period:
            line += f", period={self.period}"
        line += ")"
        out = [line]
        for c in self.children:
            out.append(c.render_tree(depth + 1))
        return "\n".join(out)



# Module 2: TOC Bottleneck Analysis  (Goldratt 1984, 1990)
# ════════════════════════════════════════════════════════════════════

@dataclass
class BottleneckReport:
    """Per-WC bottleneck assessment."""
    work_center_id: str
    work_center_code: str
    peak_utilization: float
    peak_period_idx: int
    avg_utilization: float
    is_bottleneck: bool  # peak_util > BOTTLENECK_THRESHOLD
    elevation_options: List[str] = field(default_factory=list)
    shadow_price_minutes: float = 0.0  # gain from +1 min capacity

    def to_dict(self) -> dict:
        return {
            "work_center_id": self.work_center_id,
            "work_center_code": self.work_center_code,
            "peak_utilization": self.peak_utilization,
            "peak_period_idx": self.peak_period_idx,
            "avg_utilization": self.avg_utilization,
            "is_bottleneck": self.is_bottleneck,
            "elevation_options": self.elevation_options,
            "shadow_price_minutes": self.shadow_price_minutes,
        }


# TOC threshold per Schragenheim & Ronen (1990): utilization > 0.85 begins
# to compound delay via Kingman's formula effects (queueing theory).
BOTTLENECK_UTILIZATION_THRESHOLD = 0.85


def identify_bottlenecks_from_loads(loads, work_centers_dict) -> List[BottleneckReport]:
    """Given CRP loads from v3.26, identify bottleneck work-centers.

    TOC five-focusing-steps applied:
      (1) Identify constraint        ← this function
      (2) Exploit                    ← elevation_options suggestion
      (3) Subordinate                ← caller's responsibility (scheduling)
      (4) Elevate                    ← counterfactual_capacity()
      (5) Repeat

    Args:
      loads: List[WorkCenterLoad] from capacity_aware_mrp
      work_centers_dict: {wc_id → WorkCenter ORM object}

    Returns:
      List[BottleneckReport] sorted by peak_utilization descending.
    """
    # Group by work-center
    by_wc: Dict[str, list] = {}
    for L in loads:
        by_wc.setdefault(L.work_center_id, []).append(L)

    reports: List[BottleneckReport] = []
    for wc_id, wc_loads in by_wc.items():
        wc = work_centers_dict.get(wc_id)
        if wc is None:
            continue

        utilizations = [L.utilization for L in wc_loads]
        peak_idx = max(range(len(utilizations)), key=lambda i: utilizations[i])
        peak_util = utilizations[peak_idx]
        avg_util = sum(utilizations) / max(1, len(utilizations))

        is_bn = peak_util > BOTTLENECK_UTILIZATION_THRESHOLD

        # Elevation options per Goldratt 1984 step (4)
        elevation = []
        if is_bn:
            elevation.append(
                f"加班：本 WC 尖峰利用率 {peak_util:.1%} "
                f"於期間 {peak_idx}，可考慮加班或第三班"
            )
            if wc.alternate_group:
                elevation.append(
                    f"使用替代機台群 {wc.alternate_group}"
                )
            elevation.append(
                "外包：部分工序外發第三方加工"
            )
            elevation.append(
                f"購買 / 升級機台（資本決策）— 若年化超載則 ROI 可成立"
            )

        # Shadow price: marginal throughput gain from +1 min capacity
        # Heuristic: at overload, every +1 min directly produces 1 more
        # min of output (linear region). Below capacity, shadow price = 0.
        shadow_price = 1.0 if is_bn else 0.0

        reports.append(BottleneckReport(
            work_center_id=wc_id,
            work_center_code=wc.code,
            peak_utilization=peak_util,
            peak_period_idx=peak_idx,
            avg_utilization=avg_util,
            is_bottleneck=is_bn,
            elevation_options=elevation,
            shadow_price_minutes=shadow_price,
        ))

    reports.sort(key=lambda r: -r.peak_utilization)
    return reports


async def identify_bottlenecks(
    db: AsyncSession, mps_id: str,
) -> Tuple[List[BottleneckReport], "CapacityPlanResult"]:
    """High-level: run capacity-aware MRP, then identify bottlenecks.

    Returns (reports, full_capacity_result).
    """
    from app.services.capacity_aware_mrp import run_capacity_aware_mrp

    capacity_result = await run_capacity_aware_mrp(db, mps_id)

    wcs = (await db.execute(
        select(WorkCenter).where(WorkCenter.is_active == True)
    )).scalars().all()
    wc_dict = {wc.id: wc for wc in wcs}

    reports = identify_bottlenecks_from_loads(capacity_result.loads, wc_dict)
    return reports, capacity_result



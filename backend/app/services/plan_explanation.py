# Module 1: Demand Provenance — explain a planned order's "why"
# ════════════════════════════════════════════════════════════════════

async def explain_planned_order(
    db: AsyncSession,
    mrp_item_id: str,
    max_depth: int = 5,
) -> ExplanationNode:
    """Trace upstream from a planned MrpItem to its root demand causes.

    Walks back through:
      MRP item → its parent product's planned releases
                 → that product's MPS entries
                 → optionally any SOs linking to MPS entries

    Returns a tree rooted at the queried MrpItem; children = direct causes.

    Cycle protection: max_depth limits recursion.

    Complexity: O(D · F) where D = max_depth, F = avg fan-in.
    """
    item = (await db.execute(
        select(MrpItem).where(MrpItem.id == mrp_item_id)
    )).scalar_one_or_none()
    if item is None:
        return ExplanationNode(
            node_type="error",
            entity_id=mrp_item_id,
            label=f"MrpItem 不存在 / not found: {mrp_item_id}",
            qty=0,
        )

    # Load the Part for human-readable label
    part = (await db.execute(
        select(Part).where(Part.id == item.part_id)
    )).scalar_one_or_none()
    part_label = (
        f"{part.part_no} ({part.name})" if part
        else f"Part:{item.part_id[:8]}"
    )

    root = ExplanationNode(
        node_type="mrp_item",
        entity_id=item.id,
        label=f"📦 {part_label}",
        qty=item.planned_order_release,
        period=item.period,
        detail={
            "gross_requirement": item.gross_requirement,
            "net_requirement": item.net_requirement,
            "projected_on_hand": item.projected_on_hand,
            "bom_level": item.bom_level,
            "order_type": item.order_type,
        },
    )

    if max_depth <= 0:
        return root

    # Step 1: Find parent products whose BOM uses this part
    # (i.e., who triggered this part's gross requirement)
    parent_bom_items = (await db.execute(
        select(BOMItem).where(
            BOMItem.part_id == item.part_id,
            BOMItem.is_active == True,
        )
    )).scalars().all()

    parent_product_ids = {bom.product_id for bom in parent_bom_items}

    if not parent_product_ids:
        # Top-level part (no parent) — link to MPS directly
        mps_node = await _explain_via_mps(db, item.part_id, item.period, item.mrp_master_id)
        if mps_node:
            root.children.append(mps_node)
        return root

    # Step 2: For each parent product, find its planned releases in the same MRP
    parents_explained = []
    for prod_id in parent_product_ids:
        parent_part = (await db.execute(
            select(Part).where(Part.part_no.in_(
                # use part_no == product_no convention
                select(Product.product_no).where(Product.id == prod_id)
            ))
        )).scalar_one_or_none()
        if parent_part is None:
            continue

        # Find the parent's planned release in the same MRP master
        parent_mrp = (await db.execute(
            select(MrpItem).where(
                MrpItem.mrp_master_id == item.mrp_master_id,
                MrpItem.part_id == parent_part.id,
            )
        )).scalar_one_or_none()
        if parent_mrp is None:
            continue

        # Recurse
        sub = await explain_planned_order(db, parent_mrp.id, max_depth - 1)
        # Annotate the edge: this child uses N units per parent
        bom_link = next(
            (b for b in parent_bom_items if b.product_id == prod_id),
            None
        )
        if bom_link:
            sub.detail["link_qty_per"] = bom_link.qty_per
            sub.detail["link_scrap_rate"] = bom_link.scrap_rate
            sub.label = (
                f"⬆ 因為要做 {sub.label}（每件用 {bom_link.qty_per:g}, "
                f"耗損 {(bom_link.scrap_rate or 0):.1%}）"
            )
        parents_explained.append(sub)

    root.children.extend(parents_explained)

    # If no parents found via BOM, fallback to MPS direct link
    if not parents_explained:
        mps_node = await _explain_via_mps(db, item.part_id, item.period, item.mrp_master_id)
        if mps_node:
            root.children.append(mps_node)

    return root


async def _explain_via_mps(
    db: AsyncSession, part_id: str, period: str, mrp_master_id: str,
) -> Optional[ExplanationNode]:
    """Find MPS entries that might have caused this part's demand."""
    # First, find the Product corresponding to this part (by part_no == product_no)
    part = (await db.execute(
        select(Part).where(Part.id == part_id)
    )).scalar_one_or_none()
    if part is None:
        return None
    product = (await db.execute(
        select(Product).where(Product.product_no == part.part_no)
    )).scalar_one_or_none()
    if product is None:
        return None

    # Find MPS master for this run
    mrp = (await db.execute(
        select(MrpMaster).where(MrpMaster.id == mrp_master_id)
    )).scalar_one_or_none()
    if mrp is None:
        return None

    mps_entries = (await db.execute(
        select(MpsEntry).where(
            MpsEntry.mps_master_id == mrp.mps_master_id,
            MpsEntry.product_id == product.id,
        )
    )).scalars().all()

    if not mps_entries:
        return None

    mps_node = ExplanationNode(
        node_type="mps_entry_set",
        entity_id=mrp.mps_master_id,
        label=f"📋 MPS：{product.product_no} ({product.name})",
        qty=sum(e.planned_production for e in mps_entries),
        period=period,
        children=[
            ExplanationNode(
                node_type="mps_entry",
                entity_id=e.id,
                label=f"📆 期間 {e.period}",
                qty=e.planned_production,
                period=e.period,
                detail={
                    "forecast_demand": e.forecast_demand,
                    "actual_demand": e.actual_demand,
                    "available_to_promise": e.available_to_promise,
                },
            )
            for e in mps_entries
        ],
    )
    return mps_node


